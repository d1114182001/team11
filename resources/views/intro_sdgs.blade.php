    <h1>可持續發展目標 (SDGs)</h1>
    <p>可持續發展目標（Sustainable Development Goals, SDGs）是聯合國於2015年制定的17項全球性目標，旨在解決貧困、不平等和氣候變化等問題，促進可持續發展，直到2030年。</p>

    <h2>17項可持續發展目標</h2>
    <div class="goal">
        <h3>1. 消除貧窮</h3>
        <p>在全球範圍內消除一切形式的貧窮。</p>
    </div>
    <div class="goal">
        <h3>2. 零飢餓</h3>
        <p>消除饑餓，實現食物安全，改善營養，促進可持續農業。</p>
    </div>
    <div class="goal">
        <h3>3. 良好健康與福祉</h3>
        <p>確保健康的生活方式，促進所有年齡層的福祉。</p>
    </div>
    <div class="goal">
        <h3>4. 優質教育</h3>
        <p>確保包容性和公平的優質教育，促進終身學習機會。</p>
    </div>
    <div class="goal">
        <h3>5. 性別平等</h3>
        <p>實現性別平等，賦予所有女性和女孩權力。</p>
    </div>
    <div class="goal">
        <h3>6. 清潔飲水與衛生設施</h3>
        <p>確保全世界人們能夠獲得水和衛生設施的可持續管理。</p>
    </div>
    <div class="goal">
        <h3>7. 經濟適用的清潔能源</h3>
        <p>確保所有人都能獲得經濟適用的、可靠的、可持續的現代能源。</p>
    </div>
    <div class="goal">
        <h3>8. 體面工作和經濟增長</h3>
        <p>促進持久、包容和可持續的經濟增長，實現充分和富有生產力的就業。</p>
    </div>
    <div class="goal">
        <h3>9. 產業、創新和基礎設施</h3>
        <p>建設具備彈性基礎設施，促進可持續產業化，並推動創新。</p>
    </div>
    <div class="goal">
        <h3>10. 減少不平等</h3>
        <p>在國家內部和國際之間，減少不平等。</p>
    </div>
    <div class="goal">
        <h3>11. 可持續城市和社區</h3>
        <p>確保包容、安全、有彈性和可持續的城市與人類住區。</p>
    </div>
    <div class="goal">
        <h3>12. 負責任的消費和生產</h3>
        <p>確保可持續的消費和生產模式。</p>
    </div>
    <div class="goal">
        <h3>13. 氣候行動</h3>
        <p>採取緊急行動應對氣候變化及其影響。</p>
        <img src="/pictures/test.png" alt="Cover Image"/>
    </div>
    <div class="goal">
        <h3>14. 海洋生態</h3>
        <p>保護和可持續利用海洋和海洋資源。</p>
    </div>
    <div class="goal">
        <h3>15. 陸地生態</h3>
        <p>保護、恢復和促進可持續利用陸地生態系統，管理森林可持續利用，打擊沙漠化，制止土地退化和生物多樣性喪失。</p>
    </div>
    <div class="goal">
        <h3>16. 和平與公正</h3>
        <p>促進包容的社會，提供可持續發展的公正、和平和包容的環境。</p>
    </div>
    <div class="goal">
        <h3>17. 夥伴關係促進目標實現</h3>
        <p>加強執行手段，振興全球夥伴關係，以促進可持續發展。</p>
    </div>

    <h2>結語</h2>
    <p>實現可持續發展目標需要全球各國、企業和個人的共同努力。我們每個人都可以為實現這些目標做出貢獻，讓世界變得更美好。</p>

